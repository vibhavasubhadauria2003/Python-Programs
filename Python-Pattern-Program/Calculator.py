a=input("Enter first Number ")
op=input("Enter operator ")
b= input("Enter second number ")
if op=="+":
    t=int(a)+int(b)
    print(t)
if op=="-":
    t=int(a)-int(b)
    print(t)
if op=="*":
    t=int(a)*int(b)
    print(t)
if op=="/":
    t=int(a)/int(b)
    print(t)
if op=="%":
    t=int(a)%int(b)
    print(t)